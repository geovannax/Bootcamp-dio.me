package dio.me;

public class calculadora {

    public static  void soma(float a , float b){
        float resultado =a+b;
        System.out.println("O resultado da soma e="+resultado);
    }
    public static void subtracao(float a, float b){
        float resultado =a-b;
        System.out.println("O resultado da subtracao e="+resultado);
    }
    public static void multiplicacao(float a, float b){
        float resultado =a*b;
        System.out.println("O resultado da multiplicacao e="+resultado);
    }
    public static void divisao(float a, float b){
        float resultado =a/b;
        System.out.println("O resultado da divisao e="+resultado);
    }

}
